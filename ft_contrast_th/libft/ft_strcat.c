#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int len;

	i = -1;
	len = (int)ft_strlen(s1);
	while (s2[++i])
		s1[len++] = s2[i];
	s1[len] = '\0';
	return (s1);
}
